import UIKit

extension UIFont {
    static let title = UIFont.boldSystemFont(ofSize: 24)
    static let medium = UIFont.systemFont(ofSize: 24, weight: .medium)
    static let normal = UIFont.systemFont(ofSize: 16, weight: .regular)
    static let smallText = UIFont.systemFont(ofSize: 12, weight: .light)
}
